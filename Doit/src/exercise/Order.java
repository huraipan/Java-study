package exercise;

public class Order {
	
	long orderNum;
	String userID;
	int orderYear;
	int orderMonth;
	int orderDay;
	String name;
	String productNum;
	String adress;
	
	public long getOrderNum() {
		return orderNum;
	}
	
	public String getUserID() {
		return userID;
	}
	
	public int getOrderYear() {
		return orderYear;
	}
	
	public int getOrderMonth() {
		return orderMonth;
	}
	
	public int getOrderDay() {
		return orderDay;
	}
	
	public String getName() {
		return name;
	}
	
	public String getProductNum() {
		return productNum;
	}
	
	public String getAdress() {
		return adress;
	}

}
