package study;

public class OperExam07 {

	public static void main(String[] args) {
		
		
		int a=10, result=0;
		
		result+=a;
		System.out.println(result);
		result-=a;
		System.out.println(result);
		result*=a;
		System.out.println(result);
		result/=a;
		System.out.println(result);
		result%=a;
		System.out.println(result);

	}

}
