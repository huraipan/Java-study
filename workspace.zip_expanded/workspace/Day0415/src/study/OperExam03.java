package study;

/*  비교연산: >, <, <=, >=
 *   항등 관계연산: ==, !=
 *   
 * 
 *  
 */



public class OperExam03 {

	public static void main(String[] args) {
		
		
		int i=10, j=20;
		
		
		boolean c=i<j;  // 변수 i가 j보다 작다
		
		System.out.println("i<j: "+c);
		System.out.println();
		System.out.println();
		c=i!=j; // 변수 i와 변수 j는 같지 않다.
		System.out.println("i!=j: "+c);
		
		
		
		

	}

}
